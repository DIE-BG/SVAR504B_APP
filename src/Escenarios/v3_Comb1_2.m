%% 1. JCCF: Anclaje de IPEI proveniente de QPM en horizonte de pronóstico
% (8 Trimestres)

% Base de datos
alt1 = databank.fromCSV(...
        fullfile('data', 'corrimientos',MODEL.CORR_DATE,...
                 'v1', 'fulldata_QPM.csv')); 
             
% Trimestres de anclaje
MODEL.DATES.E1_dates = MODEL.DATES.pred_start:MODEL.DATES.pred_start+7;

%% %%%%%%%%%%%%%%%% Creación de escenario alternativo %%%%%%%%%%%%%%%%%%%%%%
% Shocks del escenario base
shocks = MODEL.F_pred*get(MODEL.MF, 'elist');
% Concatenación de bd con condiciones iniciales (MODEL.F) y shocks Esc.
% Libre
MODEL.Esc.v3.dbi = dboverlay(MODEL.F,shocks);

% Imposición de anclajes provenientes del QPM en base de datos
MODEL.Esc.v3.dbi.d4_ln_ipei(MODEL.DATES.E1_dates) = alt1.D4L_CPI_RW(MODEL.DATES.E1_dates);
MODEL.Esc.v3.dbi.i(MODEL.DATES.pred_start) = MODEL.F.i(MODEL.DATES.hist_end);


% Plan de simulación
MODEL.Esc.v3.planSim = plan(MODEL.MF,MODEL.DATES.pred_start:MODEL.DATES.pred_end);
% Variable a endogenizar (shock propio?? No necesariamente)
MODEL.Esc.v3.planSim = endogenize(MODEL.Esc.v3.planSim,{'s_d4_ln_ipei'},MODEL.DATES.E1_dates); 
MODEL.Esc.v3.planSim = endogenize(MODEL.Esc.v3.planSim,{'s_i'},MODEL.DATES.pred_start); 
% Variable a exogenizar (Anclaje)
MODEL.Esc.v3.planSim = exogenize(MODEL.Esc.v3.planSim,{'d4_ln_ipei'},MODEL.DATES.E1_dates);
MODEL.Esc.v3.planSim = exogenize(MODEL.Esc.v3.planSim,{'i'},MODEL.DATES.pred_start);

% Simulación.

MODEL.Esc.v3.pred = simulate(MODEL.MF,...
                  MODEL.Esc.v3.dbi,...
                  MODEL.DATES.pred_start:MODEL.DATES.pred_end,...
                  'plan',MODEL.Esc.v3.planSim,...
                  'anticipate',false,...
                  'DbOverlay=', true);
              
% Descomposición             
MODEL.Esc.v3.shd = simulate(MODEL.MF,...
                  MODEL.Esc.v3.pred,...
                  MODEL.DATES.hist_start:MODEL.DATES.pred_end,...
                  'anticipate',false,...
                  'contributions',true);  
              
%% Post Procesamiento
MODEL = PostProcessing(MODEL,...
                       'list',pp_list,...
                       'list_niv', list_nivel,...
                       'Esc',{'v3', MODEL.Esc.v3.pred});
          
%% GRÁFICAS
